SELECT
    (CASE
        WHEN EXTRACT(MONTH FROM DIFFERENTIATION_DATE) IN (1, 2, 3) THEN '1Q'
        WHEN EXTRACT(MONTH FROM DIFFERENTIATION_DATE) IN (4, 5, 6) THEN '2Q'
        WHEN EXTRACT(MONTH FROM DIFFERENTIATION_DATE) IN (7, 8, 9) THEN '3Q'
        WHEN EXTRACT(MONTH FROM DIFFERENTIATION_DATE) IN (10, 11, 12) THEN '4Q'
    END) AS QUARTER,
    COUNT(*) AS ECOLI_COUNT
FROM
    ECOLI_DATA
GROUP BY
    QUARTER